//        Math class
//        alcuni metodi: abs, min, max, pow, sqrt
//        attributi: PI

public class Main {
    public static void main(String[] args) {
        System.out.println(Math.abs(-55)); //valore assoulto
        System.out.println(Math.min(55,21)); //minimo tra 2 numeri
        System.out.println(Math.max(55,21)); //massimo tra 2 numeri
        System.out.println(Math.pow(2,3)); //potenza base, esponente
        System.out.println(Math.sqrt(9)); //radice quadrata
        System.out.println(Math.PI); //pi greco

    }
}
